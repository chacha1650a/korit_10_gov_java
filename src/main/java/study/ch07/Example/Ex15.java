package study.ch07.Example;

public class Ex15 {
    public static void main(String[] args) {
        int season = 3;

        if (season == 1) {
            System.out.println("봄입니다.");
        } else if (season == 2) {
            System.out.println("여름입니다.");
        } else if (season == 3) {
            System.out.println("가을입니다.");
        } else if (season == 4) {
            System.out.println("겨울입니다.");
        } else {
            System.out.println("잘못 된 입력입니다.");
        }
    }
}
